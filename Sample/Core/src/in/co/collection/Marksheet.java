package in.co.collection;

public class Marksheet {
	public String fname;
	public String lname;
	public String rollno;
	
	
	
	
	
	
	  public Marksheet(String f,String l, String r) 
	  { fname= f;
	  lname=l; 
	  rollno=r;}
	  
	  public String toString() { 
		  return fname+","+lname+","+rollno;
	  
	  }
	 

	
}
